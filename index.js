let number;
let rightAnswer;
let rightAnswerArr;
let hiddenAnswer;
let hiddenAnswerArr;
let answerPrg = document.querySelector("#answer");
let cluePrg = document.querySelector("#clue");
let live;
let flag;
let restLives = document.querySelector("#lives");

function getRandomInt(max) {
  number = Math.floor(Math.random() * max);
}

function startQuiz() {
  cluePrg.innerHTML = "click hint to get a clue";
  live = 9;
  restLives.innerHTML = `You have ${live} lives`;

  getRandomInt(arr.length);

  let questionPrg = document.querySelector("#question");
  questionPrg.innerHTML = arr[number].question;

  rightAnswer = arr[number].answer;
  rightAnswerArr = rightAnswer.split("");

  hiddenAnswer = arr[number].answer;
  hiddenAnswerArr = hiddenAnswer.split("");
  for (let i = 0; i < hiddenAnswerArr.length; i++) {
    if (hiddenAnswerArr[i] != " ") {
      hiddenAnswerArr[i] = "_";
    }
  }

  answerPrg.innerHTML = hiddenAnswerArr.join("");
}

function showClue() {
  cluePrg.innerHTML = arr[number].clue;
}

function playAgain() {
  location.reload();
}

function checkLetter(event) {
  if (event.target.className == "letter") {
    event.target.classList.remove("letter");
    event.target.classList.add("btn");
    flag = false;

    for (let i = 0; i < rightAnswerArr.length; i++) {
      if (event.target.innerHTML == rightAnswerArr[i].toLowerCase()) {
        hiddenAnswerArr[i] = rightAnswerArr[i];
        answerPrg.innerHTML = hiddenAnswerArr.join("");
        flag = true;
      }
    }

    if (hiddenAnswerArr.toString() == rightAnswerArr.toString()) {
      let result = document.querySelector("#result");
      let win = document.querySelector(".win");
      result.style.display = "block";
      win.style.display = "block";
    }

    if (flag == false) {
      live--;
      restLives.innerHTML = `You have ${live} lives`;
      if (live < 1) {
        restLives.innerHTML = `Game over`;
        let result = document.querySelector("#result");
        let lose = document.querySelector(".lose");
        result.style.display = "block";
        lose.style.display = "block";
      }
    }
  }
}

let letterBtn = document.querySelector("#alphabet");
letterBtn.addEventListener("click", checkLetter);

startQuiz();
